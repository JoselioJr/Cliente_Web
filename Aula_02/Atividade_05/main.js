let nome = "Rodrigo";

document.writeln('<h1>Utilizando ".charAt" para selecionar a letra do String de acordo com o índice</h1>');
document.writeln('<p>'+nome.charAt(2)+'</p>');

document.writeln('<h1>Utilizando ".split" para separar a String a cada ""</h1>');
document.writeln('<p>'+nome.split("")+'</p>');

document.writeln('<h1>Utilizando ".toUpperCase" para deixar a String em letras maiúsculas</h1>');
document.writeln('<p>'+nome.toUpperCase()+'</p>');